const normalizeSpaces = (value) => value.replace(/\s+/g, ' ').trim();
const translationLookupMaps = {};

export const fixEncodingArtifacts = (value) => {
    if (typeof value !== 'string') return value;

    return encodingFixes.reduce(
        (current, [broken, fixed]) => current.split(broken).join(fixed),
        value
    );
};

export const normalizeTranslationKey = (value) => normalizeSpaces(fixEncodingArtifacts(value));

const canonicalizeTranslationKey = (value) => normalizeTranslationKey(value)
    .replace(/&aacute;/g, 'á')
    .replace(/&eacute;/g, 'é')
    .replace(/&iacute;/g, 'í')
    .replace(/&oacute;/g, 'ó')
    .replace(/&uacute;/g, 'ú')
    .replace(/&ntilde;/g, 'ñ')
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/^[¿¡]\s*/, '')
    .replace(/^[^a-z0-9]+/i, '')
    .toLowerCase();

const getTranslationLookupMap = (language) => {
    if (!translationLookupMaps[language]) {
        translationLookupMaps[language] = Object.entries(translations[language] || {}).reduce(
            (map, [key, translatedValue]) => map.set(canonicalizeTranslationKey(key), translatedValue),
            new Map()
        );
    }

    return translationLookupMaps[language];
};

