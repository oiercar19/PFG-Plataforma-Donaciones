export const translateText = (value, language) => {
    if (!value || typeof value !== 'string') return value;

    const fixedValue = fixEncodingArtifacts(value);
    if (language === 'es') {
        const normalizedSpanish = normalizeTranslationKey(value);
        return translations.es[normalizedSpanish]
            || getTranslationLookupMap('es').get(canonicalizeTranslationKey(value))
            || fixedValue;
    }

    const normalized = normalizeTranslationKey(value);
    const directTranslation = translations[language]?.[normalized];
    if (directTranslation) return directTranslation;

    const canonicalTranslation = getTranslationLookupMap(language).get(canonicalizeTranslationKey(value));
    if (canonicalTranslation) return canonicalTranslation;

    const matchedPattern = patternTranslations[language]?.find(({ pattern }) => pattern.test(normalized));
    if (matchedPattern) {
        return normalized.replace(matchedPattern.pattern, matchedPattern.replace);
    }

    return fixedValue;
};
